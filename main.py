#For Loop with Range
#Imprime del 1 al 9
#Incrementa por 1
for number in range(1,10):
  print(number)

#For Loop with Range
#Imprime del 1 al 9
#Incrementa por 3
for number in range(1,10,3):
  print(number)

#Sumar todos los numeros de 1 a 100
total = 0
for number in range (1,101):
  total += number

print(total)
